package com.indivaragroup.operator.compare;

public class ComparingOperator {
    public static void comparingOperatorShow() {

        int a = 5;
        int b = 11;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
